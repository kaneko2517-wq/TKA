def main():
    path = INPUT_CSV
    if path.lower().endswith(('.xlsx', '.xls')):
        df = pd.read_excel(path)
    else:
        df = pd.read_csv(path, encoding='utf-8', engine='python', on_bad_lines='skip')

    # 列名末尾の余分な空白を消去（CSVの読み込みで改行混入等があるケース対策）
    df.columns = df.columns.str.strip()

    # データフレーム中の列名を対象パターンで自動検出してスコア化
    used_cols = set()

    for cfg in COLUMN_CONFIG:
        pattern = cfg.get("pattern")
        columns = cfg.get("columns")

        if not pattern and not columns:
            continue

        if columns is not None:
            targets = [c for c in df.columns if c in columns]
        else:
            if cfg.get("exact", False):
                targets = [c for c in df.columns if c == pattern]
            else:
                targets = [c for c in df.columns if pattern in c]

        if len(targets) == 0:
            key = columns if columns is not None else pattern
            print(f"INFO: マッチ対象なし -> {key}")
            continue

        for c in targets:
            out = f"{c}_score"
            used_cols.add(c)

            s = df[c].astype(str).str.strip()
            df[out] = s.map(cfg["map"])

            if cfg.get("missing_value") is not None:
                df[out] = df[out].fillna(cfg["missing_value"])

            print(f"変換: {c} -> {out}")

    if len(used_cols) == 0:
        print("WARNING: 対応列が0件です。COLUMN_CONFIG の pattern を確認してください。")
    df.to_csv(OUTPUT_CSV, index=False, encoding="utf-8")
    print("完了:", OUTPUT_CSV)

if __name__ == "__main__":
    main()